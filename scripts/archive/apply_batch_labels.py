def parse_labels() -> dict[int, str]:
    """Parse the batch labels into a dictionary."""
    labels = {}
    for line in BATCH_LABELS.strip().split("\n"):
        id_str, label = line.split(",")
        labels[int(id_str)] = label.lower()
    return labels


def apply_labels():
    """Apply labels to the JSONL file."""
    data_path = Path("data/trigger_labeling.jsonl")

    if not data_path.exists():
        print(f"Error: {data_path} not found")
        return

    labels = parse_labels()
    print(f"Loaded {len(labels)} labels (IDs {min(labels.keys())}-{max(labels.keys())})")

    # Read all lines
    lines = []
    with open(data_path) as f:
        for line in f:
            lines.append(json.loads(line.strip()))

    # Apply labels
    applied = 0
    for entry in lines:
        entry_id = entry.get("id")
        if entry_id in labels:
            entry["label"] = labels[entry_id]
            applied += 1

    # Write back
    with open(data_path, "w") as f:
        for entry in lines:
            f.write(json.dumps(entry) + "\n")

    print(f"Applied {applied} labels to {data_path}")

    # Show distribution
    label_counts: dict[str, int] = {}
    for entry in lines:
        label = entry.get("label")
        if label:
            label_counts[label] = label_counts.get(label, 0) + 1

    print("\nLabel distribution (all labeled entries):")
    for label, count in sorted(label_counts.items(), key=lambda x: -x[1]):
        print(f"  {label}: {count}")

    # Count unlabeled
    unlabeled = sum(1 for e in lines if e.get("label") is None)
    print(f"\nUnlabeled entries remaining: {unlabeled}")


if __name__ == "__main__":
    apply_labels()
